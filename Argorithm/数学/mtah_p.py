def dist(x1, y1, x2, y2):
    return ((x1 - x2) ** 2) + ((y1 - y2) ** 2) ** 0.5


for i in range(N):
    mx = 0
    ans = 0
    x1, y1 = A[i]
    for j in range(N):
        x2, y2 = A[j]
        d = dist(x1, y1, x2, y2)
        if mx < d:
            mx = d
            ans = j + 1
    print(ans)


